"""
Ask two numbers, do the four basic mathematic operations and show the rusults.
"""
print ("Ingress two numbers and obtain the results of sum, rest, multiplication and division")
number1 = int(input("Number 1: "))
number2 = int(input("Number 2: "))

print (f"Sum: {number1} + {number2} = {number1+number2}")
print (f"Rest: {number1} - {number2} = {number1-number2}")
print (f"Multiplication: {number1} x {number2} = {number1*number2}")
print (f"Division: {number1} / {number2} = {number1/number2}")
